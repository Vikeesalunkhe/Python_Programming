if num==1:
    print("Monday")
elif num==2:
    print("Tuesday")
elif num==3:
    print("Wensday")
elif num==4:
    print("Thursday")
elif num==5:
    print("Fraiday")
elif num==6:
    print("Saturday")
elif num==7:
    print("Sunday")
else:
    print("invalid number")

